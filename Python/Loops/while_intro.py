# while loop is used to execute a block of code repeatedly as long as a given condition is true.
# It is often used when the number of iterations is not known beforehand.

# Example: Print numbers from 1 to 5
count = 1

while count <= 5:
    print("count:", count)
    count += 1

# Example: Print numbers from 5 to 1
count1 = 5

while count1 >= 1:
    print("count1:", count1)
    count1 -= 1